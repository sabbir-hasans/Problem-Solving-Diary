#include<stdio.h>
int main(){
    int a = 10;
    int b = 5;
    int sum = a + b;
    printf("Summation = %d\n", sum);
    int sub = a - b;
    printf("subtraction = %d\n", sub);
    int mul = a * b;
    printf("Multiplication = %d\n", mul);
    int div = a / b;
    printf("Division = %d\n", div);
    int mod = a % b;
    printf("Modulas = %d\n", mod);

    return 0;
}